package day17_0716;

public class Developer {

	// 加己
	private String name;
	private int period;

	// 积己磊
	Developer(String name, int period) {
		this.name = name;
		this.period = period;
	}

	// 阿 加己狼 get,set Method
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getPeriod() {
		return period;
	}

	public void setPeriod(int period) {
		this.period = period;
	}

}
